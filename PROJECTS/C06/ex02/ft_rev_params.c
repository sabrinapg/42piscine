#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int argc, char *argv[])
{
	int	parameters;

	parameters = argc - 1;
	while (parameters > 0)
	{
		ft_putstr(argv[parameters]);
		ft_putchar('\n');
		parameters--;
	}
	return (0);
}
