#include <unistd.h>

void	final(char f[5])
{
	if (f[0] == '7' && f[1] == '8' && f[2] == '9')
	{
		write(1, f, 3);
	}
	else
		write(1, f, 5);
}

void	ft_print_comb(void)
{
	char	f[5];

	f[0] = 48;
	f[3] = 44;
	f[4] = 32;
	while (f[0] <= 55)
	{
		f[1] = f[0] + 1;
		while (f[1] <= 56)
		{
			f[2] = f[1] + 1;
			while (f[2] <= 57)
			{
				final(f);
				f[2]++;
			}
			f[1]++;
		}
		f[0]++;
	}
}

