#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;
	int	m;

	i = 0;
	m = argc - 1;
	if (argc > 1)
	{
		while (*argv[m])
			write (1, argv[m]++, 1);
	}
	write (1, "\n", 1);
	return (0);
}
